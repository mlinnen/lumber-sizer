using System;
using System.Collections.Generic;

namespace WWA.Core.Models
{
    /// <summary>
    /// Options flags for the packer.
    /// </summary>
    [Flags]
    public enum PackerOptions
    {
        None = 0,
        PreferLongRemnants = 1
    }

    /// <summary>
    /// Request object for the packer. Contains the cutlist, a snapshot of inventory and packing constraints.
    /// Example:
    /// var req = new PackingRequest { CutList = cutList, Inventory = inventory, Constraints = constraints, Seed = 42 };
    /// </summary>
    public class PackingRequest
    {
        public CutList CutList { get; set; } = new CutList();
        public Inventory Inventory { get; set; } = new Inventory();
        public Constraints Constraints { get; set; } = new Constraints();
        public int? Seed { get; set; }
        public PackerOptions Options { get; set; } = PackerOptions.None;
    }

    /// <summary>
    /// Result of a packing operation.
    /// </summary>
    public class PackingResult
    {
        /// <summary>
        /// Allocations by board.
        /// </summary>
        public List<BoardAllocation> Allocations { get; } = new List<BoardAllocation>();

        /// <summary>
        /// Leftover remnants (unused parts of boards) represented as Boards with remaining lengths.
        /// </summary>
        public List<Board> Leftovers { get; } = new List<Board>();

        /// <summary>
        /// Cut items that could not be placed on any board.
        /// </summary>
        public List<CutItem> UnplacedItems { get; } = new List<CutItem>();

        /// <summary>
        /// Waste percent (0-100) computed as total leftover length divided by original total board length.
        /// </summary>
        public double WastePercent { get; set; }

        /// <summary>
        /// Total length used for cuts (in inches).
        /// </summary>
        public double TotalUsedLength { get; set; }

        /// <summary>
        /// Total number of boards used (allocations).
        /// </summary>
        public int TotalBoardsUsed => Allocations.Count;

        /// <summary>
        /// Total leftover length across all remnants.
        /// </summary>
        public double TotalWasteLength { get; set; }

        /// <summary>
        /// The deterministic seed used by the packer (may be the provided seed or one generated by the packer).
        /// </summary>
        public int? DeterministicSeedUsed { get; set; }

        /// <summary>
        /// Optional runtime counters/metrics produced by packers (e.g., loop counts, iterations).
        /// </summary>
        public Dictionary<string, long> Counters { get; } = new Dictionary<string, long>();
    }

    /// <summary>
    /// Allocation of cut placements on a specific board.
    /// </summary>
    public class BoardAllocation
    {
        public Guid BoardId { get; set; }
        public double OriginalBoardLength { get; set; }
        public List<Placement> Placements { get; } = new List<Placement>();

        /// <summary>
        /// 2D placements for packers that produce X/Y placements. May be empty for 1D packers.
        /// </summary>
        public List<Placement2D> Placements2D { get; } = new List<Placement2D>();

        /// <summary>
        /// Remaining length after placements.
        /// </summary>
        public double RemnantLength { get; set; }
    }

    /// <summary>
    /// A placement of a CutItem on a board at an offset. Rotation indicates whether the item was rotated to fit.
    /// </summary>
    public class Placement
    {
        public Guid CutItemId { get; set; }
        public CutItem? CutItem { get; set; }
        public double Offset { get; set; }
        public bool Rotated { get; set; }
        public double Length { get; set; }
    }

    /// <summary>
    /// A 2D placement of a CutItem on a board.
    /// Coordinates are in inches from the board origin (0,0) at one corner: X along length, Y along width.
    /// </summary>
    public class Placement2D
    {
        public Guid CutItemId { get; set; }
        public CutItem? CutItem { get; set; }
        public double XOffset { get; set; }
        public double YOffset { get; set; }
        public double Width { get; set; }
        public double Length { get; set; }
        public bool Rotated { get; set; }
    }
}